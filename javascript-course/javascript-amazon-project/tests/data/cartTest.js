import { addToCart, cart, loadFromStorage, removeFromCart, updateDeliveryOption } from '../../data/cart.js';

describe('Test Suite: addToCart', () => {

  // 16e
  beforeEach(() => {
    spyOn(localStorage, 'setItem'); // Mock the setItem so that the addToCart used below will no longer save anything to localStorage.
  });

  it('Adds an existing product to the cart', () => {
    
    spyOn(localStorage, 'getItem').and.callFake(() => { // overRiding getItem(Mocking it)
      return JSON.stringify([{
          productId: 'e43638ce-6aa0-4b85-b27f-e1d07eb678c6',
          quantity: 1,
          deliveryOptionId: '1'
      }]); // will convert to JSON string
    });
    loadFromStorage();

    addToCart('e43638ce-6aa0-4b85-b27f-e1d07eb678c6');
    expect(cart.length).toEqual(1);
    expect(localStorage.setItem).toHaveBeenCalledTimes(1); // gives us an object having many useful methods
    expect(cart[0].productId).toEqual('e43638ce-6aa0-4b85-b27f-e1d07eb678c6');
    expect(cart[0].quantity).toEqual(2);

    // 16c
    expect(localStorage.setItem).toHaveBeenCalledWith('cart', JSON.stringify([{ // 16c
      productId: 'e43638ce-6aa0-4b85-b27f-e1d07eb678c6',
      quantity: 2,
      deliveryOptionId: '1'
    }]));
  });

  it('Adds a new product to the cart', () => {

    spyOn(localStorage, 'getItem').and.callFake(() => { // overRiding getItem(Mocking it)
      return JSON.stringify([]); // will convert to JSON string
    });
    // console.log(localStorage.getItem());
    loadFromStorage();
    
    addToCart('e43638ce-6aa0-4b85-b27f-e1d07eb678c6');
    expect(cart.length).toEqual(1);
    expect(localStorage.setItem).toHaveBeenCalledTimes(1); // gives us an object having many useful methods
    expect(cart[0].productId).toEqual('e43638ce-6aa0-4b85-b27f-e1d07eb678c6');
    expect(cart[0].quantity).toEqual(1);

    // 16d
    expect(localStorage.setItem).toHaveBeenCalledWith('cart', JSON.stringify([{ // 16d
      productId: 'e43638ce-6aa0-4b85-b27f-e1d07eb678c6',
      quantity: 1,
      deliveryOptionId: '1'
    }]));
  });
});

// 16i
describe('Test Suite: removeFromCart', () => {

  beforeEach(() => {
    spyOn(localStorage, 'setItem');

    spyOn(localStorage, 'getItem').and.callFake(() => {
      return JSON.stringify([{
        productId: 'e43638ce-6aa0-4b85-b27f-e1d07eb678c6',
        quantity: 1,
        deliveryOptionId: '1'
      }]);
    });
    loadFromStorage();
  });

  it('Removes an Existing Product from cart', () => {
    
    removeFromCart('e43638ce-6aa0-4b85-b27f-e1d07eb678c6');
    
    expect(cart.length).toEqual(0);

    expect(localStorage.setItem).toHaveBeenCalledTimes(1);

    expect(localStorage.setItem).toHaveBeenCalledWith('cart', JSON.stringify([]));
  });

  it('Does nothing if product is not in the cart', () => {

    removeFromCart('does-not-exist');
    expect(cart.length).toEqual(1);
    expect(cart[0].productId).toEqual('e43638ce-6aa0-4b85-b27f-e1d07eb678c6');
    expect(cart[0].quantity).toEqual(1);
    expect(localStorage.setItem).toHaveBeenCalledTimes(1);
    expect(localStorage.setItem).toHaveBeenCalledWith('cart', JSON.stringify([{
      productId: 'e43638ce-6aa0-4b85-b27f-e1d07eb678c6',
      quantity: 1,
      deliveryOptionId: '1'
    }]));
  });
});

